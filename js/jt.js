var app={
	w:640,
	h:480,
	
	groundedBefore:false,
	roomBefore:0,
	
	room:0,
	slide:0,
	crouched:false,
	forceCrouch:false,
	
	lerp:0.25,
	respawn:function(){
		player.grav=0;
		player.jumps=player.jumpsMax;
		jt.stopPlay("hurt");
		app.setup()
	},
	//setup is called when the game has finished loading
	setup:function(){
		//virtual camera active
		jt.camactive(true);
		jt.volume(0.5)
		//jt.fullscreen(false);
		player.x=32+(app.room*jt.w());
		player.y=jt.h()/3;
		
		this.createRoom(app.room);
	},
	createRoom:function(room){
		//create room
		walls=[];
		var w=jt.w()
		var h=jt.h()
		var u=32;
		
		var before=room-1;
		var after=room+1;
		
		if(before<0){before=0;}
		if(after>=rooms.length){after=rooms.length-1}
		
		//make the rooms
		for(var i=before;i<after+1;i++){
			//current room
			if(i<=rooms.length-1){
				var room=rooms[i]
				
				for(var y=0;y<room.length;y++){
					for(var x=0;x<room[y].length;x++){
						var offset=(i*w)
						if(room[y][x]==1){
							addWall(x*u+offset,y*u,u,u)
						}else if(room[y][x]==2){
							addWall(x*u+offset,y*u,u,u,true)
						}else if(room[y][x]==3){
							addWall(x*u+offset,y*u,u,u,false,true)
						}else if(room[y][x]==4){
							addWall(x*u+offset,y*u,u,u,true,true)
						}
					}
				}
			}
		}
	},
	//update is called every frame
	update:function(){
		//UPDATE
		
		
		if(app.roomBefore!=app.room){
			this.createRoom(app.room);
		}
		
		//move camera
		if(app.roomBefore<app.room){
			jt.cam().x=jt.lerp(jt.cam().x,app.room*jt.w(),app.lerp)
		}else if(app.roomBefore>app.room){
			jt.cam().x=jt.lerp(jt.cam().x,app.room*jt.w(),app.lerp)
		}
		
		//check if grounded
		var grounded=false;
		var respawn=false;
		var ice=false;
		var crouch=false;
		var water=false;
		
		//create a tenporary player to test collisions
		var p={x:player.x,y:player.y+player.h/2+1,w:player.w,h:player.h/2}
		
		//loop through all the walls to check if grounded
		for(var i=0;i<walls.length;i++){
			//get current wall
			var wall=walls[i];
			//check collision
			if(jt.cRect(p,wall)){
				if(!wall.lava){
					player.jumps=player.jumpsMax;
					grounded=true;
					player.grav=0;
					player.y=wall.y-player.h
					
					if(!jt.cRect(player,wall)){
						player.y=wall.y-player.h
					}
					respawn=false;
					
					if(wall.ice){
						ice=true;
					}
				}else{
					if(wall.ice){
						water=true;
					}else{
						respawn=true;
					}
				}
			}
		}
		
		if(grounded){respawn=false;}
		
		
		crouch=false;
		if(grounded && jt.kCheck("down") && !pressing){
			crouch=true;
		}
		
		if(!this.crouched && crouch){
			player.y+=player.hCrouch;
			p.y+=player.hCrouch;
		}
		
		var cantJump=false;
		
		if(this.crouched && !crouch && grounded){
			//check if block above
			var ceiling=false;
			var standing={x:player.x,y:player.y-player.hCrouch,w:player.w,h:player.hStand}
			for(var i=0;i<walls.length;i++){
				//get current wall
				var wall=walls[i];
				//check collision
				if(jt.cRect(standing,wall)){
					if(!wall.lava){
						ceiling=true;
					}else{
						if(wall.ice){
							water=true;
						}else{
							respawn=true;
						}
					}
				}
			}
			if(ceiling){
				respawn=false;
				crouch=true;
			}else{
				player.y-=player.hCrouch;
				p.y-=player.hCrouch;
			}
		}
		
		if(crouch){
			if(jt.kPress("up") || jt.kPress("z")){
				//check if block above
				var ceiling=false;
				var standing={x:player.x,y:player.y-player.hCrouch,w:player.w,h:player.hStand}
				for(var i=0;i<walls.length;i++){
					//get current wall
					var wall=walls[i];
					//check collision
					if(jt.cRect(standing,wall)){
						if(!wall.lava){
							ceiling=true;
						}
					}
				}
				if(ceiling){
					cantJump=true;
					
				}
			}
			
			player.h=player.hCrouch
			p.h=player.hCrouch
		}else{
			player.h=player.hStand
			p.h=player.hStand
		}
		
		this.crouched=crouch;
		
		//move
		var moving=false;
		var pressing=false;
		var dir=0;
		if(jt.kCheck("left") && !crouch){
			dir--;
		}
		if(jt.kCheck("right") && !crouch){
			dir++;
		}
		
		if(dir!=0){
			pressing=true;
			moving=true;
			if(grounded){
				if(player.walk%12==0){
					jt.stopPlay("walk");
				}
				player.walk++;
			}
		}else{
			pressing=false;
			player.walk=0;
			if(this.slide!=0){
				moving=true;
				dir=Math.sign(this.slide);
			}
		}
		
		var move=player.speed*dir;
		
		p.y=player.y;
		p.x+=move;
		
		//check horizontal collisions
		//loop through all the walls to check if grounded
		var wall=false;
		for(var i=0;i<walls.length;i++){
			//get current wall
			var wall=walls[i];
			//check collision
			if(jt.cRect(p,wall)){
				if(!wall.lava){
					this.slide=0;
					move=0;
					p.x-=player.speed*dir;
					respawn=false;
					wall=true;
				}else{
					if(wall.ice){
						water=true;
					}else{
						respawn=true;
					}
				}
			}
		}
		
		if(wall){respawn=false;}
		
		player.x=p.x;
		//check if changed room
		if(player.x+player.w/2<jt.w()*app.room){
			player.x=-player.w/2+(jt.w()*app.room)
		}
		if(player.x+player.w/2>jt.w()*(app.room+1)){
			app.roomBefore=app.room;
			app.room++;
		}
		
		if(water){
			player.jumps=1;
			player.gravity=player.gravityWater;
		}else{
			player.gravity=player.gravityAir
		}
		
		//fall if not grounded
		if(!grounded){
			//remove first jump
			if(player.jumps>1){
				player.jumps=1;
			}
			player.grav+=player.gravity;
		}
		
		
		if((grounded || player.jumps>=1) && !cantJump && (jt.kPress("up") || jt.kPress("z"))){
			var jumpHeight=player.jump;
			if(crouch){
				
				jumpHeight=player.jumpCrouch
				crouch=false;
				p.y-=player.hCrouch;
				player.h=player.hStand;
			}
			player.grav=-jumpHeight
			player.jumps--;
			jt.stopPlay("jump");
			grounded=false;
		}
		
		p.y+=player.grav;
		
		//check vertical collisions
		//loop through all the walls to check if grounded
		var ceiling=false;
		for(var i=0;i<walls.length;i++){
			//get current wall
			var wall=walls[i];
			//check collision
			if(jt.cRect(p,wall)){
				if(!wall.lava){
					if(player.grav<0){
						player.grav=0;
						p.y=wall.y+wall.h;
						
					}else if(player.grav>0){
						if(wall.ice){
							ice=true;
						}
						player.grav=0;
						p.y=wall.y-player.h
					}
					respawn=false;
					ceiling=true;
				}else{
					if(wall.ice){
						water=true;
					}else{
						respawn=true;
					}
				}
			}
		}
		
		if(water){
			player.jumps=1;
			player.gravity=player.gravityWater;
		}else{
			player.gravity=player.gravityAir
		}
		
		if(ceiling){respawn=false;}
		
		player.y=p.y;
		
		
		player.y=jt.stay(player.y,0,jt.h()-player.h);
		
		if(this.groundedBefore==false && grounded==true){
			jt.stopPlay("walk")
		}
		this.groundedBefore=grounded;
		
		if(ice){
			this.slide=move;
		}else{
			this.slide=0;
		}
		
		
		//DRAWING
		
		//draw white background
		jt.bg("white");
		
		//draw all walls
		for(var i=0;i<walls.length;i++){
			//get current wall
			var wall=walls[i];
			var c="grey"
			if(wall.lava){
				c="red"
			}
			if(wall.ice){
				c="lightblue"
				if(wall.lava){
					c="blue"
				}
			}
			jt.rect(wall.x,wall.y,wall.w,wall.h,c)
		}
		
		//write text
		jt.font("Consolas",64);
		jt.text("BOXFACE",jt.w()/2,40,"black","center")
		jt.font("Consolas",24);
		jt.text("Left and right arrow to walk",jt.w()/2,100,"black","center")
		jt.text("Z or Up to jump/double jump",jt.w()/2,130,"black","center")
		jt.text("Down to crouch",jt.w()/2+(jt.w()*15),60,"black","center")
		jt.text("Jump while crouching for more height",jt.w()/2+(jt.w()*21),60,"black","center")
		jt.text("X and R to respawn",jt.w()/2,160,"black","center")
		
		//draw player
		if(grounded){
			if(moving && pressing){
				jt.anim("walk",player.x-16,player.y-8,player.w+32,player.h+8)
			}else if(crouch){
				jt.image("crouch",player.x-16,player.y-8-player.hCrouch,player.w+32,player.h+player.hCrouch+8)
			}else{
				jt.image("idle",player.x-16,player.y-8,player.w+32,player.h+8)
			}
		}else{
			jt.image("jump",player.x-16,player.y-8,player.w+32,player.h+8)
		}
		if(jt.kPress("-")){
			app.roomBefore=app.room;
			app.room--;
			respawn=true;
			
		}
		if(jt.kPress("=")){
			app.roomBefore=app.room;
			app.room++;
			respawn=true;
		}
		
		if(respawn || jt.kPress("r") || jt.kPress("x")){
			this.groundedBefore=true;
			this.respawn();
		}
	}
}


